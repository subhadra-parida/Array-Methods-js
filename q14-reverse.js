// The reverse() method reverses the elements in an array. we can use it to sort an array
//  in descending order..........
const name=["Rakhi", "Mantu", "Barsha", "Lisha", "Pintu", "Payal"];
name.sort();
console.log(name);
name.reverse();
console.log(name);

// ["Rakhi", "Mantu", "Barsha", "Lisha", "Pintu", "Payal"]
// ["Payal", "Pintu", "Lisha", "Barsha", "Mantu", "Ra"]