// The values() method returns a new array iterator object that contains the values for each 
// index in the array................

const array1 = ['Odisha', 'Jajpur', 'Kuakhia'];
const result = array1.values();
for (const value of result) {
  console.log(value);
}




// OUTPUT:-
// Odisha
// Jajpur
// Kuakhia
